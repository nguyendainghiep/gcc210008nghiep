    private void fillToTable(){
        tbModel.setRowCount(0);
        for(phone p : pList){
            Object[] row = new Object[] {p.getPname(),p.getPid(),p.getRam(),
                                        p.getRom(),p.getBrand() ,p.getAmount(),
                                        p.getStatus()};
            tbModel.addRow(row);
    }
    }
    private void showMess(String mess){
        JOptionPane.showMessageDialog(this, mess);
    }
    private boolean loadFile(){
        if(libFile.readObject(filePath)==null){
            return false;
        }
        pList = (ArrayList<phone>) libFile.readObject(filePath);
        return true;
    }
    
    private void initTable(){
        String[] columnsName = {"Phone","ID","Ram","Rom",
                                "Brand","Amount","Status"};
        tbModel = new DefaultTableModel(columnsName,0);
        tbPhone.setModel(tbModel);
        }
    
    private  void loadCb(){
        String[] ramLst = {"Choose ram","4Gb","6Gb",
                            "8Gb", "10Gb"};
        for (String string : ramLst){
            ramModel.addElement(string);
        }
        cbRam.setModel(ramModel); 
        
        String[] brList = {"Choose brand","Apple","Samsung",
                            "Xiaomi", "Oppo","LG","Nokia"};
        for (String string : brList){
            brModel.addElement(string);
        }
        cbBrand.setModel(brModel);
        
        String[] romLst = {"Choose rom","64Gb","128Gb",
                            "512GB", "1T"};
        for (String string : romLst){
            romModel.addElement(string);
        }
        cbRom.setModel(romModel);
        
        String[] staLst = {"New","LikeNew","99%",
                            "Old", "Supper Old"};
        for (String string : staLst){
            staModel.addElement(string);
        }
        cbStatus.setModel(staModel);
    }


