    private void saveTb(){
        libFile.writeObject(filePath, pList);
    }
    private void exitBtn(){
        if (JOptionPane.showConfirmDialog( this,"Do you want to exit now ???","Warning !!",
            JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION)
            System.exit(0);
    }
    private void addPhone(){
        String pName = txtPName.getText();
        String pId = txtPId.getText();
        String Amount = txtAmount.getText();
        String brand = cbBrand.getSelectedItem().toString();
        String ram = cbRam.getSelectedItem().toString();
        String rom = cbRom.getSelectedItem().toString();
        String stat = cbStatus.getSelectedItem().toString();
        phone b = new phone(pName, pId, ram, rom, brand, Amount, stat);
        pList.add(b);
    }
    
    private void delRow(){
        int opdel = JOptionPane.showConfirmDialog(this,
                "Do you want to delete this row?", "Delete", 
                JOptionPane.YES_NO_OPTION,
                JOptionPane.WARNING_MESSAGE);
            if(opdel == JOptionPane.YES_OPTION){
                pList.remove(currentRow);
            }
    }
    
    private void updatePhone(){
        phone c = pList.get(currentRow);
        c.setPname(txtPName.getText());
        c.setPid(txtPId.getText());
        c.setBrand(cbBrand.getSelectedItem().toString());
        c.setRam(cbRam.getSelectedItem().toString());
        c.setRom(cbRom.getSelectedItem().toString());
        c.setAmount(txtAmount.getText());
        c.setStatus(cbStatus.getSelectedItem().toString());
        
    }
    private void resetTb(){
        txtPName.setText(null);
        txtPId.setText(null);
        txtAmount.setText(null);
        cbBrand.setSelectedIndex(0);
        cbRam.setSelectedIndex(0);
        cbRom.setSelectedIndex(0);
    }
    
    private void disP(int r){
        String pname = (String) tbPhone.getValueAt(r, 0);
        txtPName.setText(pname);
        String pid = (String) tbPhone.getValueAt(r, 1);
        txtPId.setText(pid);
        String ram = (String) tbPhone.getValueAt(r, 2);
        cbRam.setSelectedItem(ram);
        String rom = (String) tbPhone.getValueAt(r, 3);
        cbRom.setSelectedItem(rom);
        String brand = (String) tbPhone.getValueAt(r, 4);
        cbBrand.setSelectedItem(brand);
        String amount = (String) tbPhone.getValueAt(r, 5);
        txtAmount.setText(amount);
        String stat = (String) tbPhone.getValueAt(r, 6);
        cbStatus.setSelectedItem(stat);
    }

